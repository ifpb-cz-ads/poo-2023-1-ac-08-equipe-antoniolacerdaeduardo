public class Equipamento {
    private String nome;
    private double valor;

    public void setNome(String nome){
        this.nome = nome;
    }
    public void setValor(double valor){
        this.valor = valor;
    }

    public String getNome(){
        return this.nome;
    }
    public double getValor(){
        return this.valor;
    }
}
